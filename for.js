
//for (inicialización; condición; incremento/decremento)
//La condicion en este caso es X < 5

var X = 0;
for (X; X < 5; X++)  {
   console.log ("el valor de X es:" + X)   //Esta es la sentencia de incremento
}

var X = 10;
for (X; X > 0; X--)  {
   console.log ("el valor de X es:" + X)   //Esta es la sentencia de decremento
}
